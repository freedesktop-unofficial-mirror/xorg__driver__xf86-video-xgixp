#include "config.h"
#endif

#include "xgi.h"
#include "xgi_regs.h"
#include "xg47_cursor.h"

Bool XGIHWCursorInit(ScreenPtr pScreen)
{
    ScrnInfoPtr pScrn = xf86Screens[pScreen->myNum];
    XGIPtr      pXGI = XGIPTR(pScrn);
    Bool        ret = FALSE;

    switch(pXGI->chipset)
    {
    case XG47:
        ret = XG47HWCursorInit(pScreen);
        break;
    default:
        break;
    }

    return ret;
}
